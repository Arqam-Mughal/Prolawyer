                            title: 'Payment Method Error',
                            text: 'Please select a payment method!'
                        });
                error = 1;
            }

            if (response.exists == 1) {
                Swal.fire({
                    icon: 'error',
                    title: 'Oops...',
                    text: 'This email is already taken!'
                });
            }

            if (response.exists == 0) {
                if (error == 0) {
                    // $('.nextStep').hide();
                    // $('.step-1').slideUp();
                    // $('.step-2').slideDown();

                    // The items the customer wants to buy
                    const items = $("#staff_addForm").serialize();

                    // Check the payment method

                    if (selectedPaymentMethod === 'payu') {
                        // Start PayU payment process
                        generatePayUOrder(items);
                    } else if (selectedPaymentMethod === 'cashfree') {
                        // Start Cashfree payment process
                        startCashfreePayment(items);
                    }

                } else {
                    $(".errorsList").remove();
                    var errors = `<div class="col-md-12 errorsList"><div class="alert alert-danger"><ul>`;
                    errors += (name == '') ? `<li>Please fill name field.</li>` : ``;
                    errors += (email == '') ? `<li>Please fill email field.</li>` : ``;
                    errors += (phone == '') ? `<li>Please fill phone field.</li>` : ``;
                    errors += (phone.length > 10 || phone.length < 9) ? `<li>Invalid Phone Number</li>` : ``;
                    errors += (password == '') ? `<li>Please fill password field.</li>` : ``;
                    errors += (password.length < 8) ? `<li>Password must be at least 8 characters.</li>` : ``;
                    errors += (confirm_password == '') ? `<li>Please fill confirm password field.</li>` : ``;
                    errors += (confirm_password != password) ? `<li>Password and Confirm password does not match.</li>` : ``;
                    errors += (!specialCharRegex.test(password) || !numericRegex.test(password)) ? `<li>Password must contain at least one special character and one numeric character.</li>` : ``;
                    errors += `</ul></div></div>`;
                    $('.step-1').prepend(errors);
                }
            }

            if (response.msg) {
                Swal.fire({
                    icon: 'error',
                    title: 'Oops...',
                    text: 'Email field not found!'
                });
            }
        },
        error: function(error) {
            Swal.fire({
                icon: 'error',
                title: 'Oops...',
                text: "Something went wrong!",
            });
        }
    });
});

function generatePayUOrder(items) {

    $.ajax({
        url: "/generate-payu-order",  // The endpoint where your backend will handle PayU order creation
        type: "POST",
        data: {
            "_token": "{{ csrf_token() }}",
            items: items
        },
        success: function(response) {
            if (response.status === 'success') {
                startPayUPayment(response.order_data);
            } else {
                alert('Error: ' + response.message);
            }
        },
        error: function(xhr, status, error) {
            console.error('Error occurred while creating PayU order:', error);
            alert('An error occurred while generating the PayU order.');
        }
    });
}

// Function to initiate PayU payment
function startPayUPayment(orderData) {
    // console.log(orderData);

    var form = $('<form action="https://test.payu.in/_payment" method="POST">');
    $.each(orderData, function(key, value) {
        form.append($('<input>', {
            type: 'hidden',
            name: key,
            value: value
        }));
    });
    form.appendTo('body').submit();
}

function startCashfreePayment(items) {
    // Make an AJAX call to fetch Cashfree payment details
    $.ajax({
        url: "{!! route('cashfree.pay.store') !!}",
        data: {
            "_token": "{{ csrf_token() }}",
            items: items
        },
        type: "POST",
        success: function(response) {

            const cashfree = Cashfree({
                mode: "sandbox",  // Test in sandbox environment
                // mode: "production",  // Test in sandbox environment
            });

            // Define the checkout options
            let checkoutOptions = {
                paymentSessionId: response.session_id,
                redirectTarget: "_self", // Change to "_blank" if needed
            };

            cashfree.checkout(checkoutOptions);
        },
        error: function(error) {
            console.log("Payment Error:", error); // Debugging error details
            Swal.fire({
                icon: 'error',
                title: 'Payment Failed',
                text: "Something went wrong with Cashfree integration.",
            });
        }
    });
}

